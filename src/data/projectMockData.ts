// @ts-nocheck
// Types
export interface ChecklistItem {
  id: string;
  text: string;
  checked: boolean;
}

export interface SubTask {
  id: string;
  name: string;
  owner: string;
  status: 'Not Started' | 'In Progress' | 'Completed' | 'Blocked';
  priority: 'Low' | 'Medium' | 'High' | 'Critical';
  dueDate: string;
  // New optional fields (matching Task's data points)
  description?: string;
  milestoneId?: string;
  startDate?: string;
  clientVisible?: boolean;
  estimatedHours?: number;
  actualHours?: number;
  assignedEmployees?: string[];
  assignedPartners?: string[];
  dependencies?: string[];
  comments?: TaskComment[];
  isCriticalPath?: boolean;
  checklist?: ChecklistItem[];
}

export interface TaskComment {
  id: string;
  author: string;
  avatar: string;
  content: string;
  timestamp: string;
}

export interface Task {
  id: string;
  milestoneId: string;
  name: string;
  owner: string;
  startDate: string;
  endDate: string;
  status: 'Not Started' | 'In Progress' | 'Pending Approval' | 'Completed' | 'Blocked';
  priority: 'Low' | 'Medium' | 'High' | 'Critical';
  isCriticalPath: boolean;
  dependencies: string[];
  clientVisible: boolean;
  assignedEmployees: string[];
  assignedAssets: string[];
  assignedPartners: string[];
  estimatedHours: number;
  actualHours: number;
  subtasks: SubTask[];
  comments: TaskComment[];
  description: string;
}

export interface Milestone {
  id: string;
  projectId: string;
  name: string;
  startDate: string;
  endDate: string;
  status: 'Not Started' | 'In Progress' | 'Completed' | 'At Risk';
  color: string;
  tasks: Task[];
}

export interface CostCategory {
  budget: number;
  committed: number;
  actual: number;
}

export interface ChangeOrder {
  id: string;
  title: string;
  requestedBy: string;
  date: string;
  budgetImpact: number;
  status: 'Pending' | 'Approved' | 'Rejected';
  description: string;
}

export interface PurchaseOrder {
  id: string;
  vendor: string;
  description: string;
  amount: number;
  status: 'Draft' | 'Sent' | 'Acknowledged' | 'Invoiced' | 'Paid';
  issueDate: string;
  dueDate: string;
  category: string;
}

export interface TeamMember {
  id: string;
  name: string;
  role: string;
  department: string;
  allocation: number;
  startDate: string;
  endDate: string;
  avatar?: string;
}

export interface AIRisk {
  id: string;
  severity: 'Low' | 'Medium' | 'High' | 'Critical';
  category: 'Timeline' | 'Budget' | 'Resource' | 'Quality' | 'Dependency';
  title: string;
  description: string;
  affectedTask?: string;
  recommendedAction: string;
  detectedDate: string;
}

export interface Project {
  id: string;
  name: string;
  dealId: string;
  client: string;
  eventName: string;
  eventDate: string;
  eventVenue: string;
  startDate: string;
  endDate: string;
  status: 'Planning' | 'In Progress' | 'Completed' | 'On Hold';
  projectDirector: string;
  accountManager: string;
  totalBudget: number;
  revenue: number;
  overheadAllocation: number;
  financials: {
    categories: {
      labour_internal: CostCategory;
      labour_partner: CostCategory;
      equipment: CostCategory;
      logistics: CostCategory;
      venue: CostCategory;
      contingency: CostCategory;
    };
  };
  milestones: Milestone[];
  team: TeamMember[];
  changeOrders: ChangeOrder[];
  purchaseOrders: PurchaseOrder[];
  risks: AIRisk[];
}

